# Inicializa a variável saldo com o valor 
saldo = 200
print(saldo)  

# Multiplica saldo por 20
saldo *= 20
print(saldo)  

# Realiza a divisão inteira de saldo por 10
saldo //= 10
print(saldo)  

# Subtrai 100 de saldo
saldo -= 100
print(saldo)  #
# Divide saldo por 5
saldo /= 5
print(saldo)  

# Adiciona 10 a saldo
saldo += 10
print(saldo)  

# Eleva saldo ao quadrado
saldo **= 2
print(saldo)    

# Calcula o resto da divisão de saldo por 200
saldo %= 200
print(saldo)  


